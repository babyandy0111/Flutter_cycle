class Post {
  final String userImage;
  final String username;
  final String postImage;
  final String caption;
  final List<String> thumbnail;
  final String avatarUrl;
  final int likeCount;
  final String nickname;
  final String abstract;
  final String createdAt;

  Post({
    this.userImage,
    this.username,
    this.postImage,
    this.caption,
    this.thumbnail,
    this.avatarUrl,
    this.likeCount,
    this.nickname,
    this.abstract,
    this.createdAt,
  });
}